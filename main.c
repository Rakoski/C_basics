#include <stdio.h>

// Among the others that C provides are \t for tab, \b for backspace, \" for the double quote
// and \\ for the backslash itself.

void main()
{
    printf("hello, ");
    printf("world");
    printf("\n");
    printf("hello, ");
    printf("world");
    printf("\t");
    printf("hello, ");
    printf("world");
    printf("\b");
    printf("hello, ");
    printf("world");
    printf("\\");
}
